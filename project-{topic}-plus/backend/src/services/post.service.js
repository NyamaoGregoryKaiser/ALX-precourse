const httpStatus = require('http-status');
const { Post, User, Category } = require('../models');
const ApiError = require('../utils/ApiError');
const logger = require('../utils/logger');
const { Op } = require('sequelize');
const redisClient = require('../utils/redisClient');

const POSTS_CACHE_KEY = 'all_posts';
const POST_CACHE_TTL = 3600; // 1 hour

/**
 * Clear posts cache
 */
const clearPostsCache = async () => {
  await redisClient.del(POSTS_CACHE_KEY);
  // Optionally, clear individual post caches if implemented
};

/**
 * Create a post
 * @param {Object} postBody
 * @param {string} authorId - ID of the user creating the post
 * @returns {Promise<Post>}
 */
const createPost = async (postBody, authorId) => {
  if (postBody.slug && (await Post.isSlugTaken(postBody.slug))) {
    throw new ApiError(httpStatus.BAD_REQUEST, 'Slug already taken');
  }
  if (!authorId) {
    throw new ApiError(httpStatus.BAD_REQUEST, 'Author ID is required');
  }
  postBody.authorId = authorId;

  // If slug is not provided, it will be generated by the model hook.
  // We still need to check for uniqueness after the hook.
  if (!postBody.slug) {
    const tempPost = Post.build(postBody);
    await tempPost.validate(); // Triggers beforeValidate hook
    if (await Post.isSlugTaken(tempPost.slug)) {
      throw new ApiError(httpStatus.CONFLICT, `Generated slug '${tempPost.slug}' is already taken. Please provide a unique slug.`);
    }
  }

  const post = await Post.create(postBody);
  await clearPostsCache();
  return post;
};

/**
 * Query for posts
 * @param {Object} filter - Sequelize filter
 * @param {Object} options - Query options like sortBy, limit, page, populate
 * @returns {Promise<QueryResult>}
 */
const queryPosts = async (filter, options) => {
  const { sortBy, limit, page, populate } = options;
  const offset = (page - 1) * limit;

  let order = [];
  if (sortBy) {
    const [field, sortOrder] = sortBy.split(':');
    order.push([field, sortOrder.toUpperCase()]);
  } else {
    order.push(['publishedAt', 'DESC']); // Default sort for published posts
  }

  const where = {};
  if (filter.title) {
    where.title = { [Op.iLike]: `%${filter.title}%` };
  }
  if (filter.slug) {
    where.slug = filter.slug;
  }
  if (filter.authorId) {
    where.authorId = filter.authorId;
  }
  if (filter.categoryId) {
    where.categoryId = filter.categoryId;
  }
  // Only show 'published' posts to general users unless explicitly filtered
  if (filter.status) {
    where.status = filter.status;
  } else if (filter.includeDrafts !== true) { // For API exposing posts, default to published
    where.status = 'published';
  }

  let include = [];
  if (populate) {
    if (populate.includes('author')) {
      include.push({
        model: User,
        as: 'author',
        attributes: ['id', 'username', 'email'],
      });
    }
    if (populate.includes('category')) {
      include.push({
        model: Category,
        as: 'category',
        attributes: ['id', 'name', 'slug'],
      });
    }
  }

  // Attempt to fetch from cache for general public (published posts without complex filters)
  if (Object.keys(where).length === 1 && where.status === 'published' && !filter.authorId && !filter.categoryId && !sortBy && !limit && !page) {
    const cachedPosts = await redisClient.get(POSTS_CACHE_KEY);
    if (cachedPosts) {
      logger.info('Serving posts from cache');
      const parsedPosts = JSON.parse(cachedPosts);
      return {
        posts: parsedPosts,
        totalResults: parsedPosts.length,
        limit: parsedPosts.length,
        page: 1,
        totalPages: 1,
      };
    }
  }

  const { count, rows: posts } = await Post.findAndCountAll({
    where,
    order,
    limit,
    offset,
    include,
  });

  // Cache results for general public if conditions met
  if (Object.keys(where).length === 1 && where.status === 'published' && !filter.authorId && !filter.categoryId && !sortBy && !limit && !page) {
    await redisClient.setEx(POSTS_CACHE_KEY, POST_CACHE_TTL, JSON.stringify(posts));
  }

  return {
    posts,
    totalResults: count,
    limit,
    page,
    totalPages: Math.ceil(count / limit),
  };
};

/**
 * Get post by ID
 * @param {string} id
 * @param {Array<string>} populate - Fields to populate (e.g., ['author', 'category'])
 * @returns {Promise<Post>}
 */
const getPostById = async (id, populate = []) => {
  let include = [];
  if (populate.includes('author')) {
    include.push({
      model: User,
      as: 'author',
      attributes: ['id', 'username', 'email'],
    });
  }
  if (populate.includes('category')) {
    include.push({
      model: Category,
      as: 'category',
      attributes: ['id', 'name', 'slug'],
    });
  }
  return Post.findByPk(id, { include });
};

/**
 * Update post by ID
 * @param {string} postId
 * @param {Object} updateBody
 * @returns {Promise<Post>}
 */
const updatePostById = async (postId, updateBody) => {
  const post = await getPostById(postId);
  if (!post) {
    throw new ApiError(httpStatus.NOT_FOUND, 'Post not found');
  }
  if (updateBody.slug && updateBody.slug !== post.slug && (await Post.isSlugTaken(updateBody.slug, postId))) {
    throw new ApiError(httpStatus.BAD_REQUEST, 'Slug already taken');
  }
  Object.assign(post, updateBody);
  await post.save();
  await clearPostsCache();
  return post;
};

/**
 * Delete post by ID
 * @param {string} postId
 * @returns {Promise<Post>}
 */
const deletePostById = async (postId) => {
  const post = await getPostById(postId);
  if (!post) {
    throw new ApiError(httpStatus.NOT_FOUND, 'Post not found');
  }
  await post.destroy();
  await clearPostsCache();
  return post;
};

// Add static method to Post model for checking existence
Post.isSlugTaken = async function (slug, excludePostId) {
  const post = await this.findOne({ where: { slug } });
  return !!post && (excludePostId ? post.id !== excludePostId : true);
};


module.exports = {
  createPost,
  queryPosts,
  getPostById,
  updatePostById,
  deletePostById,
  clearPostsCache,
};
